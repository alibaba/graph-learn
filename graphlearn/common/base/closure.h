template<bool del, typename T>
class SelfDeleter {
public:
  explicit SelfDeleter(T* obj)
    : obj_(obj)
  { }
  ~SelfDeleter() {
    if (del) {
      delete obj_;
    }
  }

private:
  SelfDeleter(const SelfDeleter&);
  SelfDeleter& operator=(const SelfDeleter&);

private:
  T* obj_;
};

